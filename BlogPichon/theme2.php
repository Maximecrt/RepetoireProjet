	1PLIKE140 démarre fin 2019 une série de freestyles, « Freestyle Batard », qui vont cumuler plusieurs millions de vues. Une influence clairement drill UK se dégage tant sur les productions que sur les flows. Mais une vraie singularité : la voix. Les foudroyantes ascensions de Koba la D ou Gambi le soulignent clairement, un timbre de voix singulier marque l’auditeur.
				</br>
		Une énergie, une impression de force tranquille se dégagent alors du jeune rappeur de 18 ans. 1PLIKE140 propose un rap brut, authentique, s’inspirant du quartier et de l’univers de la « détaille ». Des thèmes certes récurrents dans le paysage rapologique, mais une proposition musicale innovante. C’est en effet sur le « Freestyle Hors-Série », mélange des genres boom-bap et drill, entre « Survival of The Fittest » de Mobb Deep, et une instrumentale drill, qu’1PLIKE140 frappe un grand coup et explose les compteurs.
